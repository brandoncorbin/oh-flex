var app = new Vue({
  el: '#app',
  data: {
    ready: true,
    show_resource_modal: false,
    active_resource: null,
    menu_open: false,
    searchTerm: null,
    searchResults: null,
    messages: [],
    selected: {},
    title: "Brandon's Work",
    resume: resume
  },
  created: function() {
    this.selected = resume.about[0]
  },
  methods: {
    selectItem: function(project) {
      this.menu_open = false;
      this.selected = project;
      this.title = project.title;
    },
    showResource: function(resource) {
      this.show_resource_modal = true;
      this.active_resource = resource;
    },
    hideResource: function($event) {
      $event.preventDefault()
      this.show_resource_modal = false;
      this.active_resource = null;
    },
    getResourceIndex: function(resource) {
      var selected = this.selected;
      var active_resource = this.active_resource;
      var activeIndex = 0;
      var i = 0;
      this.selected.resources.forEach(function(resource) {
        if (resource == active_resource) {
          activeIndex = i;
        }
        i++;
      });
      return activeIndex;
    },
    previousResource: function() {
      let total = this.selected.resources.length;
      let currentIndex = this.getResourceIndex(this.active_resource);
      if (currentIndex >= total) {
        this.active_resource = this.selected.resources[0];
      } else if (currentIndex == 0) {
        this.active_resource = this.selected.resources[total - 1];
      } else {
        this.active_resource = this.selected.resources[currentIndex - 1];
      }
    },
    nextResource: function() {
      let total = this.selected.resources.length;
      let currentIndex = this.getResourceIndex(this.active_resource);
      if (currentIndex < (total - 1)) {
        this.active_resource = this.selected.resources[currentIndex + 1];
      } else if (currentIndex >= (total - 1)) {
        this.active_resource = this.selected.resources[0];
      } else {
        this.active_resource = this.selected.resources[0];
      }
    },
    search: function() {
      var searchResults = [];

      if (this.searchTerm) {
        var searchTerm = this.searchTerm;
        resume.experience.forEach(function(job) {
          if (JSON.stringify(job).toLowerCase().match(searchTerm)) {
            searchResults.push(job);
          }
        });
        resume.projects.forEach(function(project) {
          if (JSON.stringify(project).toLowerCase().match(searchTerm)) {
            searchResults.push(project);
          }
        });
        this.searchResults = searchResults;
      } else {
        this.searchResults = null;
      }
    },
    toggleMenu: function() {
      this.menu_open = !this.menu_open;
    }
  }
})
